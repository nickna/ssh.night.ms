CREATE UNIQUE INDEX ix_user_saved_locations_user_id_label ON public.user_saved_locations USING btree (user_id, label);
CREATE INDEX ix_user_saved_locations_user_id_sort_order ON public.user_saved_locations USING btree (user_id, sort_order);
CREATE UNIQUE INDEX ix_user_watchlist_items_user_id_canonical ON public.user_watchlist_items USING btree (user_id, canonical);
CREATE INDEX ix_user_watchlist_items_user_id_sort_order ON public.user_watchlist_items USING btree (user_id, sort_order);
CREATE INDEX ix_game_rounds_game_key_net ON public.game_rounds USING btree (game_key, net DESC);
CREATE INDEX ix_game_rounds_game_key_played_at ON public.game_rounds USING btree (game_key, played_at DESC);
CREATE INDEX ix_game_rounds_hand_id ON public.game_rounds USING btree (hand_id);
CREATE INDEX ix_game_rounds_user_id_played_at ON public.game_rounds USING btree (user_id, played_at DESC);
